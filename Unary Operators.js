// # 8. Unary Operators

// ## Definition

// Unary operators work with only one operand.

// ### Operators

// | Operator | Description |
// |----------|-------------|
// | + | Convert to Number |
// | - | Negative Value |
// | ++ | Increment |
// | -- | Decrement |
// | ! | Logical NOT |
// | typeof | Returns Data Type |
// | delete | Removes Object Property |

// ### Example

let x = "10";

console.log(+x);
console.log(typeof x);

x++;

console.log(x);

// ### Real-world Example


let visitors = 100;

visitors++;

console.log(visitors);
