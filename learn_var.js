// # `var`

// `var` is the traditional way of declaring variables in JavaScript. Before ES6 (2015), it was the only keyword available for variable declaration.

// ## Features

// - Function scoped
// - Hoisted
// - Can be redeclared
// - Can be reassigned

// ## Valid Example

var name = "Chandra";

console.log(name);

name = "Lega";   // Reassignment

console.log(name);

var name = "kavi";   // Redeclaration

console.log(name);

// **Output**

// ```
// Chandra
// Lega
// kavi
// ```
