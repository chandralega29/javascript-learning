// # 6. Ternary Operator

// ## Definition

// The ternary operator is a short form of an `if...else` statement.

// ### Syntax

// ```javascript
// condition ? value1 : value2;
// ```

// ### Example

let age = 18;

let status = age >= 18 ? "Adult" : "Minor";

console.log(status);


// ### Real-world Example

let marks = 80;

let result = marks >= 50 ? "Pass" : "Fail";

console.log(result);
