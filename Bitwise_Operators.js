// # 5. Bitwise Operators

// ## Definition

// Bitwise operators perform operations on the binary representation of numbers.

// ### Operators

// | Operator | Description |
// |----------|-------------|
// | & | AND |
// | \| | OR |
// | ^ | XOR |
// | ~ | NOT |
// | << | Left Shift |
// | >> | Right Shift |
// | >>> | Unsigned Right Shift |

// ### Example


console.log(5 & 1);
console.log(5 | 1);
console.log(5 ^ 1);



