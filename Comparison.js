// # 3. Comparison Operators

// ## Definition

// Comparison operators compare two values and return **true** or **false**.

// ### Operators

// | Operator | Description |
// |----------|-------------|
// | == | Equal (value only) |
// | === | Strict Equal |
// | != | Not Equal |
// | !== | Strict Not Equal |
// | > | Greater Than |
// | < | Less Than |
// | >= | Greater Than or Equal |
// | <= | Less Than or Equal |

// ### Example

console.log(10 > 5);
console.log(10 < 5);
console.log(10 == "10");
console.log(10 === "10");


// ### Real-world Example

let age = 20;

console.log(age >= 18);
