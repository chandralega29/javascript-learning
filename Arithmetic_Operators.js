// Arithmetic Operators

// ## Definition

// Arithmetic operators perform mathematical calculations.

// ### Operators

// | Operator | Description |
// |----------|-------------|
// | + | Addition |
// | - | Subtraction |
// | * | Multiplication |
// | / | Division |
// | % | Modulus (Remainder) |
// | ** | Exponentiation (Power) |

// ### Example

let a = 10;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** 2);

let price = 500;
let quantity = 3;

let total = price * quantity;

console.log(total);